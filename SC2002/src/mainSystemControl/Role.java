package mainSystemControl;

/**
 * Enumeration representing the various user roles in the hospital management system.
 */
public enum Role {
	/**
     * Represents a patient role.
     */
	PATIENT,
	
	/**
     * Represents a doctor role.
     */
	DOCTOR,
	
	/**
     * Represents a pharmacist role.
     */
	PHARMACIST,
	
	/**
     * Represents an administrator role.
     */
	ADMINISTRATOR
}
